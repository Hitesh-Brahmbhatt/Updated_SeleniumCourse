package javabasics;

public class Loops {

	public static void main(String[] args) {
		for (int i=0; i<10; i++) {
			System.out.println("the value of i is " +i);
		}
		
		int number=14;
		
		if(number%2==0) {
			System.out.println("even number");
		} else {
			System.out.println("odd number");
		}
}
}
